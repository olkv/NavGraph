        val btnSetting = view.findViewById(R.id.btnSettings) as Button
        btnSetting.setOnClickListener {
            //Log.d("NAVFRAG","Нажата кнопка.")
            findNavController().navigate(R.id.action_homeFragment_to_settingsFragment)
        }

    }

